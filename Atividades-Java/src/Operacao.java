public class Operacao {
    public void soma(Double x,  Double y){
        System.out.println("Soma de " + x + " + " + y + " = " + (x+y));
    }

    public void mult(Double x,  Double y){
        System.out.println("Multiplicaçao de " + x + " * " + y + " = " + (x*y));
    }

    public void divi(Double x,  Double y){
        System.out.println("Divisão de " + x + " / " + y + " = " + (x/y));
    }

    public void sub(Double x,  Double y){
        System.out.println("Subitraçao de " + x + " - " + y + " = " + (x-y));
    }
}